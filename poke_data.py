class __ai_flag(IntEnum):
	AI_FLAG_CHECK_BAD_MOVE              = (1 << 0)  # AI will avoid using moves that are likely to fail or be ineffective in the current situation.
	AI_FLAG_TRY_TO_FAINT                = (1 << 1)  # AI will prioritize KOing the player's mon if able.
	AI_FLAG_CHECK_VIABILITY             = (1 << 2)  # AI damaging moves and move effects to determine the best available move in the current situation.
	AI_FLAG_FORCE_SETUP_FIRST_TURN      = (1 << 3)  # AI will prioritize using setup moves on the first turn at the expensve of all else. AI_FLAG_CHECK_VIABILITY will instead do this when the AI determines it makes sense.
	AI_FLAG_RISKY                       = (1 << 4)  # AI will generally behave more recklessly, prioritizing damage over accuracy, explosions, etc.
	AI_FLAG_TRY_TO_2HKO                 = (1 << 5)  # AI adds score bonus to any move the AI has that either OHKOs or 2HKOs the player.
	AI_FLAG_PREFER_BATON_PASS           = (1 << 6)  # AI prefers raising its own stats and setting for / using Baton Pass.
	AI_FLAG_DOUBLE_BATTLE               = (1 << 7)  # Automatically set for double battles, handles AI behaviour with partner.
	AI_FLAG_HP_AWARE                    = (1 << 8)  # AI will favour certain move effects based on how much remaining HP it and the player's mon have.
	AI_FLAG_POWERFUL_STATUS             = (1 << 9)  # AI prefers moves that set up field effects or side statuses, even if the user can faint the target.
	# New, Trainer Handicap Flags
	AI_FLAG_NEGATE_UNAWARE              = (1 << 10)  # AI is NOT aware of negating effects like wonder room, mold breaker, etc.
	AI_FLAG_WILL_SUICIDE                = (1 << 11)  # AI will use explosion / self destruct / final gambit / etc.
	# New, Trainer Strategy Flags
	AI_FLAG_PREFER_STATUS_MOVES         = (1 << 12)  # AI gets a score bonus for status moves. Should be combined with AI_FLAG_CHECK_BAD_MOVE to prevent using only status moves.
	AI_FLAG_STALL                       = (1 << 13)  # AI stalls battle and prefers secondary damage/trapping/etc. TODO not finished.
	AI_FLAG_SMART_SWITCHING             = (1 << 14)  # AI includes a lot more switching checks. Automatically includes AI_FLAG_SMART_MON_CHOICES.
	AI_FLAG_ACE_POKEMON                 = (1 << 15)  # AI has an Ace Pokemon. The last Pokemon in the party will not be used until it's the last one remaining.
	AI_FLAG_OMNISCIENT                  = (1 << 16)  # AI has full knowledge of player moves, abilities, hold items.
	AI_FLAG_SMART_MON_CHOICES           = (1 << 17)  # AI will make smarter decisions when choosing which mon to send out mid-battle and after a KO, which are separate decisions. Automatically included by AI_FLAG_SMART_SWITCHING.
	AI_FLAG_CONSERVATIVE                = (1 << 18)  # AI assumes all moves will low roll damage.
	AI_FLAG_SEQUENCE_SWITCHING          = (1 << 19)  # AI switches in mons in exactly party order, and never switches mid-battle.
	AI_FLAG_DOUBLE_ACE_POKEMON          = (1 << 20)  # AI has *two* Ace Pokémon. The last two Pokémons in the party won't be used unless they're the last ones remaining. Goes well in battles where the trainer ID equals to twins, couples, etc.
	AI_FLAG_WEIGH_ABILITY_PREDICTION    = (1 << 21)  # AI will predict player's ability based on aiRating
	AI_FLAG_PREFER_HIGHEST_DAMAGE_MOVE  = (1 << 22)  # AI adds score to highest damage move regardless of accuracy or secondary effect
	AI_FLAG_PREDICT_SWITCH              = (1 << 23)  # AI will predict the player's switches and switchins based on how it would handle the situation. Recommend using AI_FLAG_OMNISCIENT
	AI_FLAG_PREDICT_INCOMING_MON        = (1 << 24)  # AI will score against the predicting incoming mon if it predicts the player to switch. Requires AI_FLAG_PREDICT_SWITCH
	AI_FLAG_PP_STALL_PREVENTION         = (1 << 25)  # AI keeps track of the player's switches where the incoming mon is immune to the chosen move
	AI_FLAG_PREDICT_MOVE                = (1 << 26)  # AI will predict the player's move based on what move it would use in the same situation. Recommend using AI_FLAG_OMNISCIENT
	AI_FLAG_SMART_TERA                  = (1 << 27)  # AI will make smarter decisions when choosing whether to terrastalize (default is to always tera whenever available).
	AI_FLAG_ASSUME_STAB                 = (1 << 28)  # AI knows player's STAB moves, but nothing else. Restricted version of AI_FLAG_OMNISCIENT.
	AI_FLAG_ASSUME_STATUS_MOVES         = (1 << 29)  # AI has a chance to know certain non-damaging moves, and also Fake Out and Super Fang. Restricted version of AI_FLAG_OMNISCIENT.
	AI_FLAG_ATTACKS_PARTNER             = (1 << 30)  # AI specific to double battles; AI can deliberately attack its 'partner.'
	AI_FLAG_KNOW_OPPONENT_PARTY         = (1 << 31)  # AI knows all the species in the player's party, but not moves/items/abilities unless they've been seen.

# The following options are enough to have a basic/smart trainer. Any other addtion could make the trainer worse/better depending on the flag
	AI_FLAG_BASIC_TRAINER = (AI_FLAG_CHECK_BAD_MOVE | AI_FLAG_TRY_TO_FAINT | AI_FLAG_CHECK_VIABILITY)
	AI_FLAG_SMART_TRAINER = (AI_FLAG_BASIC_TRAINER | AI_FLAG_OMNISCIENT | AI_FLAG_SMART_SWITCHING | AI_FLAG_SMART_MON_CHOICES | AI_FLAG_PP_STALL_PREVENTION | AI_FLAG_SMART_TERA)
	AI_FLAG_PREDICTION    = (AI_FLAG_PREDICT_SWITCH | AI_FLAG_PREDICT_INCOMING_MON | AI_FLAG_PREDICT_MOVE)
	AI_FLAG_ASSUMPTIONS   = (AI_FLAG_ASSUME_STAB | AI_FLAG_ASSUME_STATUS_MOVES | AI_FLAG_WEIGH_ABILITY_PREDICTION)

# 'other' ai logic flags
	AI_FLAG_DYNAMIC_FUNC = (1 << 60)  # Create custom AI functions for specific battles via "setdynamicaifunc" cmd
	AI_FLAG_ROAMING      = (1 << 61)
	AI_FLAG_SAFARI       = (1 << 62)
